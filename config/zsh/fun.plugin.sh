# shellcheck shell=sh

_dot_slap() (
	robin="$(echo "${1}" | fmt -w 28)"
	batman="$(echo "${2}" | fmt -w 26)"
	roblines="$(echo "${robin}" | wc -l)"
	batlines="$(echo "${batman}" | wc -l)"

	robskip="$(( (6-${roblines}) / 2 ))"
	batskip="$(( (6-${batlines}) / 2 ))"


	{
		echo '⡇%14s%-14s ⠙⢍⡏%13s%-13s⣽'
		echo '⡆%14s%-14s ⡑⡸⠗%13s%-13s⢿'
		echo '⡇%14s%-14s⢐⠊⡐⠢%13s%-13s⢸'
		echo '⣧%14s%-14s⣸⠁⢌⢂%13s%-13s⢸'
		echo '⡗⢷⡔%13s%-13s⢑⢽⢦⡄%13s%-13s⢺'
		echo '⡯⡲⣝⣌ %12s%-12s⠅⡠⣗⢝⢦⣈⠢ %11s%-11s⢸'
	} | awk \
		-v robin_lines="${robin}" \
		-v batman_lines="${batman}" \
		-v lines="6" \
		'
		function max(a, b) {
			return a > b ? a : b;
		}

		BEGIN {
			split(robin_lines,  robin,  "\n");
			split(batman_lines, batman, "\n");

			roblines = length(robin);
			batlines = length(batman);

			robskip = int((lines - roblines) / 2);
			batskip = int((lines - batlines) / 2);
		}

		{
			r = NR >= robskip ? robin[NR - robskip] : ""
			b = NR >= batskip ? batman[NR - batskip] : ""

			r_length = length(r);
			b_length = length(b);

			r_midpoint = int(r_length / 2 + 1);
			b_midpoint = int(b_length / 2 + 1);

			r1 = substr(r, 0, r_midpoint);
			r2 = substr(r, r_midpoint + 1, r_length);

			b1 = substr(b, 0, b_midpoint);
			b2 = substr(b, b_midpoint + 1, b_length);

			printf $0 "\n", r1, r2, b1, b2
		}
	'

	printf '⣏⢞⠼⣌⢟⡳⣳⢞⢶⢻⣌⡊        ⠣⣪⢴⡤⣥⣬⣤⢵⢖⢖⣛⢕⢕⢧⡹⣙⢗⢞⠶⡬⡦⣴⢬⢦⢬⢆⢄⢑⣄⣑⣈⣂⣂⣑⣡⣈⣂⡵⡬⢾\n'
	printf '⡇⡯⣚⢵⡹⡪⣕⢇⢷⢱⢎⢽⡪⣬⢦⡨⢢⡮⡦⢷⡙⡧⢳⢜⠖⡦⡳⡱⣣⢫⡪⡕⢧⢳⠱⡕⣝⡪⡳⡱⢞⡬⡳⡱⡣⢯⠐⣼⢱⠝⡮⡹⣍⢭⢮⡹⡪⣞⢜⢽\n'
	printf '⡇⢯⢪⢮⡪⡳⣜⢕⡳⣕⢝⢦⢫⢖⠵⣣⠈⣞⢵⡹⡜⢎⢧⢍⡏⢞⡕⡝⢖⡕⣝⢜⡣⣳⡙⡮⡪⣎⢗⢝⢎⡞⣜⢕⢯⢪⡇⡳⡕⣏⢮⢳⡱⡳⣕⢝⢮⡪⣣⢽\n'
	printf '⡏⡳⣱⢣⢝⢮⢪⢎⡞⣜⢕⢗⢵⡙⡮⣝⡤⢫⢎⢖⢝⡕⣳⡱⣙⠮⡮⡹⣕⢭⡪⡮⡱⣕⢝⢎⢞⡜⣎⢳⢕⣝⡮⣣⢗⢵⣹⢪⢞⢬⡪⣣⢫⢮⡪⣣⢳⡱⡳⢼\n'
	printf '⡟⣜⢎⣕⢳⡱⣣⢫⢎⢮⢣⡫⢎⢞⡜⢦⢺⡘⣎⢳⡱⡝⢴⢕⢝⢮⡚⡵⡱⣕⢵⡹⣪⢺⡱⡏⣞⠼⡬⡣⢧⡧⣻⡼⡵⢧⢵⣣⢫⡪⣣⢫⡞⢦⡫⣎⢧⢫⣎⢳\n'
	printf '⡗⣕⠧⣪⢳⡱⡕⣇⢗⢵⡱⣍⢳⢝⡜⣕⠧⡳⡭⣪⡪⣪⢳⡱⡫⡲⣝⢪⡓⡵⡱⡎⣾⢱⢕⢯⡪⡧⣫⣚⢕⡯⣺⣎⣟⣵⣫⢞⡽⣎⡷⣫⢏⡳⡕⣮⢪⡳⡜⣵\n'
	printf '⣗⢕⡫⡎⡶⡱⡝⣬⢚⢦⢓⢵⡱⡣⢞⡜⣪⢓⠽⡢⡳⡱⢵⣕⡝⡜⡮⡱⣝⢪⢮⢱⡏⣺⢕⡽⣼⢚⠴⡕⣵⣫⣿⣽⡿⣯⣷⣯⢮⢫⡮⡟⣬⢓⡝⢦⣫⢪⢞⢴\n'
	printf '⡧⣣⢫⡺⢜⢮⠺⡔⣏⢮⠳⡱⢎⣾⡸⡪⣪⢣⡛⣜⢕⢝⢕⢿⣹⣞⠮⣎⢮⢱⡣⡻⢼⡹⡪⡖⣎⢧⡛⣜⡞⣯⣟⣽⡟⣿⣳⣿⡗⡵⣻⢜⢎⢵⡹⡪⣖⢝⢎⠮\n'
	printf '⡯⣪⢕⢧⡛⣜⢵⡽⣽⣙⡟⣳⠷⣷⣹⢶⢵⡥⣻⡹⣟⣿⣾⣕⢯⣟⣺⣿⢣⢣⡳⣩⣻⢸⢌⡯⣪⢲⡩⢎⡿⣶⣮⢟⣹⣞⡚⣮⣟⢮⡗⣝⢎⡳⣜⢕⡵⡹⣕⠭\n'
	printf '⣯⢲⢣⢗⢝⡜⡦⢿⣶⢭⣏⢟⣳⣕⣗⢭⡻⣚⡿⡾⣺⣿⣽⣟⣻⣶⣿⣶⣧⣇⢗⢼⢔⢇⢗⢜⠦⡳⡱⣣⠇⣩⠙⠾⠵⠻⠿⢿⢜⡾⣪⢎⡮⡪⡮⡪⡞⢮⡪⡎\n'
	printf '⣿⢌⢗⡕⣏⡺⣪⢣⣿⣵⣝⠻⢮⢮⡺⢵⢝⣮⢿⡵⣺⡿⣾⣻⣟⣷⢞⢷⣟⣿⣮⢣⢎⡳⡩⣇⢏⢎⢗⢼⢐⠅⢷⢕⣬⢱⠢⣯⢳⡏⡮⣪⢪⣓⠽⣌⢯⡚⡼⡅\n'
	printf '⣗⢝⢮⢪⢮⡪⣣⢳⡌⡻⢿⣿⡵⠷⣹⡳⣽⣗⢻⣟⣿⣻⣟⣿⢟⡮⣻⣟⣽⠞⣿⣧⠫⡚⡚⡪⣪⢣⣫⣺⡔⢩⢑⠖⠢⠒⣼⣝⣽⡮⣮⢮⡣⣳⡙⡮⣪⢞⢵⡩\n'
	printf '⣯⣺⢽⣕⣵⢕⢧⡣⣝⢮⠲⣜⠻⢿⣷⣽⢺⣿⢯⣹⢛⡽⣛⢦⣛⢾⣻⣏⢿⡹⣿⣻⠪⣱⠄⡊⢳⡫⣧⡯⣻⡶⣦⣭⣵⠿⢺⠊⡿⠾⣣⣯⣷⡱⡹⡪⡮⣪⢣⡇\n'
	printf '⣷⢗⣽⣣⢟⣵⢣⢳⢜⣕⢝⣌⠻⡲⣌⣛⡛⣿⣟⣿⣵⣿⣽⣳⣿⢿⣟⣿⢏⣷⣿⣻⡇⡏⣤⡈⢫⡭⣻⡯⠾⠞⢿⠫⡲⢪⢒⢝⢬⢊⢎⠵⣞⢽⢧⡻⣸⢪⡳⡅\n'
	printf '⣿⢎⣗⡾⣹⣏⡕⢧⠳⣜⢕⢕⢝⢎⢖⢕⡭⣹⣷⡽⣳⢿⣻⡽⣿⣫⢟⣫⣺⣯⣟⣿⣻⢆⢇⡄⢍⢈⠓⣸⠬⠓⣕⢕⠕⡱⡹⢂⢇⢕⡱⢊⣊⢿⣱⠻⣷⢕⣝⡆\n'
	printf '⣿⢎⢾⣻⢕⢿⡚⣿⠱⣕⢕⡝⣎⢕⡣⡳⡪⢖⡽⣿⢿⣮⣽⣽⡽⢖⣿⣽⡿⣾⢯⣏⢮⠥⣴⡣⣎⠝⡕⡺⡠⢯⡑⣲⡋⠱⣞⣃⣭⠖⢕⣡⣣⡺⣯⢝⢮⢷⡚⡄\n'
	printf '⡿⡯⡺⣕⣯⣛⢞⣷⢝⢮⡫⣳⢼⡱⣙⢼⢊⢧⣣⣚⢿⣿⣽⣯⣿⢿⣿⣝⣿⢯⡷⡻⣸⢢⠣⡜⢕⠷⡱⣍⢪⢪⢪⢢⢝⡹⡭⡔⣞⢏⠭⠵⠶⣒⣚⣫⣫⣭⣻⡅\n'
	printf '⡗⢿⡸⣕⢧⢝⡾⣱⡟⣽⣿⢟⡮⢯⡷⣓⢏⣯⣩⡳⣕⠝⢺⡽⡻⣛⣝⢯⠻⣃⡍⡪⢸⡎⣣⢮⢎⡺⠔⣕⣱⡱⡕⣕⢦⣚⣰⡩⠕⢆⢏⢜⡱⣹⣿⣿⣗⢷⢕⡇\n'
	printf '⡟⣼⣫⣖⠯⡿⣱⣹⠺⣯⣿⣻⡾⣏⢮⣽⡕⡷⣖⣷⢵⡏⢣⠺⡼⠒⡊⡉⢍⢄⠪⡨⢍⡙⡾⠬⢦⠕⡍⢅⠔⠔⢔⠢⠢⡒⠤⡉⠝⡚⠧⣮⠢⣿⣿⣻⣿⣱⡫⡇\n'
	printf '⣏⡾⢞⣵⢟⢗⢕⠭⣯⣻⣯⢿⣟⣿⣾⣾⣽⣿⣽⣼⣵⢏⢢⡳⡰⢈⠆⡱⠐⡢⣑⣔⣢⣂⣚⠕⢭⢭⡚⡲⣽⡪⣦⣍⡢⡑⢌⢊⠌⡌⢒⢌⢙⢻⡿⣽⣿⣧⢯⡇\n'
	printf '⣹⢝⠮⡮⣽⢣⡫⡳⣩⢫⠲⡚⣯⢍⡿⠏⠛⠓⣉⡭⠌⢖⢒⢓⢘⢒⠒⠮⠭⢍⠢⡂⢆⠔⡌⡊⢺⡐⢻⠰⡐⠻⣌⠪⠪⡪⣲⣁⡜⢐⠅⡢⢑⢄⠪⢻⣷⣿⡮⡇\n'
	printf '⣣⢏⣗⢝⣺⢕⢵⡹⡢⡳⣙⡮⡶⢉⠔⡨⢊⡾⢁⠆⡱⢡⠩⣣⠢⡡⢑⠌⡢⢊⠔⡡⡑⢌⠢⡡⡟⠨⡺⢑⢌⠑⢽⡎⢘⠔⡐⠱⢧⣣⠘⡔⢡⢂⠣⣂⢙⢾⣿⡃\n'
	printf '⡯⣪⢎⢷⢹⢎⢧⢣⡽⡺⣝⣼⠡⢂⠕⡐⢼⠣⡐⢌⠢⣏⠒⡄⠳⡠⠑⢌⢐⠅⡊⢔⠡⡡⡑⢎⢌⡱⡁⠧⡈⢎⠐⡇⡱⠨⡈⡒⠔⠒⢳⠈⡆⡘⠔⢌⠢⠊⢿⡇\n'
	printf '⣺⣑⣧⣋⣻⣕⣃⣟⣜⣧⣣⣽⣀⣣⣈⣢⣈⣇⣌⣢⣈⣽⣈⣪⣨⣐⣅⣑⣄⣅⣪⣐⣑⣔⣊⢆⣒⡡⣊⡢⣊⢔⠡⡗⢡⠑⡌⢔⠡⣋⠐⢇⠌⢔⠑⡅⡩⢊⠌⡇\n'
